#include "libft.h"

void	ft_putlong(long int n)
{
	int i;
	int arr[22];

	i = 0;
	if (n == 0)
		ft_putchar('0');
	if (n < 0)
	{
		ft_putchar('-');
		if (n == -9223372036854775807)
		{
			ft_putchar('9');
			n = -223372036854775807;
		}
		n *= -1;
	}
	while (n)
	{
		arr[i] = n % 10;
		n /= 10;
		i++;
	}
	i--;
	while (i >= 0)
		ft_putchar(arr[i--] + '0');
}
